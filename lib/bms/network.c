int bms_balancing_pack(
    uint8_t *dst_p,
    const bms_balancing_t *src_p,
    size_t size)
{
    if (size < 26u) {
        return (-EINVAL);
    }

    memset(&dst_p[0], 0, 26);

    dst_p[0] |= pack_left_shift_u8(src_p->board_index, 0u, 0xffu);
    dst_p[1] |= pack_left_shift_u8(src_p->cells_cell0, 0u, 0x01u);
    dst_p[1] |= pack_left_shift_u8(src_p->cells_cell1, 1u, 0x02u);
    dst_p[1] |= pack_left_shift_u8(src_p->cells_cell2, 2u, 0x04u);
    dst_p[1] |= pack_left_shift_u8(src_p->cells_cell3, 3u, 0x08u);
    dst_p[1] |= pack_left_shift_u8(src_p->cells_cell4, 4u, 0x10u);
    dst_p[1] |= pack_left_shift_u8(src_p->cells_cell5, 5u, 0x20u);
    dst_p[1] |= pack_left_shift_u8(src_p->cells_cell6, 6u, 0x40u);
    dst_p[1] |= pack_left_shift_u8(src_p->cells_cell7, 7u, 0x80u);
    dst_p[2] |= pack_left_shift_u8(src_p->cells_cell8, 0u, 0x01u);
    dst_p[2] |= pack_left_shift_u8(src_p->cells_cell9, 1u, 0x02u);
    dst_p[2] |= pack_left_shift_u8(src_p->cells_cell10, 2u, 0x04u);
    dst_p[2] |= pack_left_shift_u8(src_p->cells_cell11, 3u, 0x08u);
    dst_p[2] |= pack_left_shift_u8(src_p->cells_cell12, 4u, 0x10u);
    dst_p[2] |= pack_left_shift_u8(src_p->cells_cell13, 5u, 0x20u);
    dst_p[2] |= pack_left_shift_u8(src_p->cells_cell14, 6u, 0x40u);
    dst_p[2] |= pack_left_shift_u8(src_p->cells_cell15, 7u, 0x80u);
    dst_p[3] |= pack_left_shift_u8(src_p->cells_cell16, 0u, 0x01u);
    dst_p[3] |= pack_left_shift_u8(src_p->cells_cell17, 1u, 0x02u);

    return (26);
}

